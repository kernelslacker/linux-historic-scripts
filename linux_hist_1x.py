from linux_hist_common import CHANGELOGS, LINUS, ROOT  # noqa: F401 (re-exported)

BINARIES = ROOT / "binaries"


@dataclasses.dataclass
class Version:
    # fmt: off
    name: str
    base: str
    date: str
    patch: str | None = None       # filename under binaries/<series>/; None means "extract the tarball"
    compression: str = "none"      # "none" -> cat, "gz" -> zcat, "bz2" -> bzcat
    chmod_writable: list[str] = dataclasses.field(default_factory=list)  # paths to chmod +w before patching
    branch_create: str | None = None    # `git branch NAME` before importing this version
    branch_checkout: str | None = None  # `git checkout NAME` before importing this version (after branch_create, if any)
    # fmt: on


def series_dir(name: str) -> str:
    if name.startswith("pre2.0"):
        return "1.3"
    for series in ("1.0", "1.1", "1.2", "1.3"):
        if name.startswith(series):
            return series
    raise ValueError(f"no known series for {name!r}")


def changelog_path(v: Version) -> Path:
    return CHANGELOGS / series_dir(v.name) / f"{v.name}.txt"


