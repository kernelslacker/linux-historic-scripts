"""Shared version table and helpers for untar-2.3.py / make-diffs-2.3.py / import-2.3.py.

Continues from 2.2's "2.2.8" (all on master, all Linus -- no branching or
author changes in this segment; 2.2's own stable maintenance continues
separately on branch "2.2"). Runs through the last "normal" 2.3.x prepatch
(2.3.52pre3), then the unusual "2.3.99-preN[-M]" numbering used as 2.4
approached, then a second prepatch scheme ("2.4.0-testNpreM") all the way
to "2.4.0-prerelease".

Like 2.0-2.2, most prepatches are cut against the last stable release
(`base` vs `patch_base` diverge); a handful of one-off patches have
colorful names ("REALLY-DANGEROUS", "for-alan-only", "for-al") preserved
literally from the original project.

The entire "2.3.99pre*" family has on-disk directory/tarball names with an
extra hyphen ("2.3.99-preN") that the canonical version/tag name drops
("2.3.99preN") -- `dir_name` carries the real on-disk name wherever it
differs; tree_dir() uses it for filesystem lookups while `name` stays the
canonical tag/diff-file name throughout.

2.3.8 (alias of 2.3.8pre3) and 2.4.0-test3 (alias of 2.4.0-test3pre9) both
get real trees/diffs built (matching the original scripts exactly, like
2.2's aliases) -- only import.py treats them as a no-op tag.

No date in import-2.3.sh is real -- same situation as 2.2, every `import`
call is missing its date argument. All dates here were recovered from
tarball mtimes / embedded diff-header timestamps. One patch (test3-pre4.gz)
had a bogus 2007 timestamp on its mm/vmscan.c hunk -- NOTES.md documents
that this hunk was hand-reconstructed by the project's original author
long after the fact, so dates >=2006 are filtered out of the max when
recovering these.
"""

